    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String hid = jTextField1.getText();
        String email = jTextField2.getText();
        String location = jTextField3.getText();
        String contact = jTextField4.getText();
        String hname = jTextField5.getText();
        Connection conn = null;
        PreparedStatement pstmt = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost/blood_bank","root","");
            String sql ="INSERT INTO hospital (H_ID, H_EMAIL, H_LOCATION, H_CONTACT_NO, H_NAME) VALUES (?, ?, ?, ?, ?) " ;
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, hid);
            pstmt.setString(2, email);
            pstmt.setString(3, location);
            pstmt.setString(4, contact);
            pstmt.setString(5, hname);
            int i = pstmt.executeUpdate();
            if (i>0)
            {
                JOptionPane.showMessageDialog(null, "SAVED !!");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "NOT SAVED !!");
            }
    }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

